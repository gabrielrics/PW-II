    <h2>3. C++: </h2>

        <p style="background-color:#7cfc00;">
        A linguagem de programação C++ é uma versão melhorada da C, e uma das mais populares linguagens na área de ciências da computação.
        Além disso, sua versatilidade faz com que a C++ seja uma das linguagens de programação mais indicadas para se aprender.
        Sua natureza rápida e potente permite que desenvolvedores criem aplicações com excelente desempenho, como videogames, softwares gráficos e navegadores web.
        </p>

    <h2>2. C#: </h2>
        
        <p style="background-color:#008b8b;">
        C# é uma linguagem de programação orientada a objetos – um modelo que organiza o design de software ao redor de objetos.
        A linguagem de programação C# é uma das principais no mercado devido ao seu rápido desempenho e estabilidade melhorada.
        Essa linguagem de programação também é mais fácil de ser aprendida do que as suas antecessoras, como C e C++,
        pois possui uma sintaxe mais simples e uma hierarquia de classe bem definida. 
        </p>

    <h2>1. Python: </h2>
        <p style="background-color:#3347ff;">
        Python é uma linguagem de programação de alto nível e para propósitos diversos.
        Ela pode ser utilizada para múltiplas tarefas, desde a análise e visualização de dados até o desenvolvimento, prototipação e automação web.
        A linguagem Python é muito popular entre desenvolvedores de software, pois funciona muito bem como uma linguagem de scripts.
        Adicionalmente, ela permite que seus usuários utilizem diferentes paradigmas de programação,
        incluindo linguagens de programação funcionais, processuais, imperativas e orientadas a objetos.
        </p>

